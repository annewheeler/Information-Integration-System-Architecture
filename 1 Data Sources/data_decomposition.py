import pandas as pd
import json

# 1) Citește fișierele sursă
country_df = pd.read_csv('/content/country_ai_index.csv')
industry_df = pd.read_csv('/content/ai_industry_summary.csv')

# 2) Păstrează doar coloanele necesare pentru MongoDB

country_cols = [
    'country',
    'region',
    'gdp_per_capita',
    'internet_penetration',
    'digital_maturity_index',
    'country_ai_policy',
    'ai_patent_filings_2024',
    'ai_researchers_per_million'
]

industry_cols = [
    'industry',
    'avg_ai_adoption_rate',
    'avg_ai_maturity_score',
    'avg_ai_failure_rate'
]

country_df = country_df[country_cols].copy()
industry_df = industry_df[industry_cols].copy()

# 3) Curăță duplicatele, dacă există
country_df = country_df.drop_duplicates(subset=['country']).reset_index(drop=True)
industry_df = industry_df.drop_duplicates(subset=['industry']).reset_index(drop=True)

# 4) Creează lista nested de benchmarkuri pe industrie
industry_benchmarks = []

for _, row in industry_df.iterrows():
    industry_doc = {
        "industry": row["industry"],
        "adoption": {
            "avg_ai_adoption_rate": None if pd.isna(row["avg_ai_adoption_rate"]) else float(row["avg_ai_adoption_rate"]),
            "avg_ai_maturity_score": None if pd.isna(row["avg_ai_maturity_score"]) else float(row["avg_ai_maturity_score"])
        },
        "risk": {
            "avg_ai_failure_rate": None if pd.isna(row["avg_ai_failure_rate"]) else float(row["avg_ai_failure_rate"])
        }
    }
    industry_benchmarks.append(industry_doc)

# 5) Creează documentele MongoDB: un document per țară
mongo_docs = []

for _, row in country_df.iterrows():
    doc = {
        "country": row["country"],
        "region": row["region"],
        "macro_indicators": {
            "gdp_per_capita": None if pd.isna(row["gdp_per_capita"]) else float(row["gdp_per_capita"]),
            "internet_penetration": None if pd.isna(row["internet_penetration"]) else float(row["internet_penetration"]),
            "digital_maturity_index": None if pd.isna(row["digital_maturity_index"]) else float(row["digital_maturity_index"])
        },
        "ai_ecosystem": {
            "country_ai_policy": row["country_ai_policy"],
            "ai_patent_filings_2024": None if pd.isna(row["ai_patent_filings_2024"]) else int(row["ai_patent_filings_2024"]),
            "ai_researchers_per_million": None if pd.isna(row["ai_researchers_per_million"]) else float(row["ai_researchers_per_million"])
        },
        "industry_benchmarks": industry_benchmarks
    }
    mongo_docs.append(doc)

# 6) Salvează ca JSON array
output_path = '/content/country_ai_profiles.json'

with open(output_path, 'w', encoding='utf-8') as f:
    json.dump(mongo_docs, f, ensure_ascii=False, indent=2)

print(f"JSON generat cu succes: {output_path}")
print(f"Număr documente: {len(mongo_docs)}")

