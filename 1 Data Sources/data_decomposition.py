import pandas as pd

# 1. Citește fișierul sursă
df = pd.read_csv('/content/ai_adoption_70k.csv')

# 2. Păstrează doar coloanele necesare
cols_needed = [
    'country',
    'survey_year',
    'quarter',
    'company_id',
    'ai_adoption_rate',
    'ai_maturity_score',
    'productivity_change_percent',
    'jobs_displaced',
    'jobs_created'
]

df = df[cols_needed].copy()

# 3. Creează agregarea
country_quarter_summary = (
    df.groupby(['country', 'survey_year', 'quarter'], as_index=False)
      .agg(
          num_companies=('company_id', 'nunique'),
          avg_ai_adoption_rate=('ai_adoption_rate', 'mean'),
          avg_ai_maturity_score=('ai_maturity_score', 'mean'),
          avg_productivity_change_percent=('productivity_change_percent', 'mean'),
          total_jobs_displaced=('jobs_displaced', 'sum'),
          total_jobs_created=('jobs_created', 'sum')
      )
)

# 4. Rotunjire pentru coloanele numerice de tip medie
country_quarter_summary['avg_ai_adoption_rate'] = country_quarter_summary['avg_ai_adoption_rate'].round(2)
country_quarter_summary['avg_ai_maturity_score'] = country_quarter_summary['avg_ai_maturity_score'].round(3)
country_quarter_summary['avg_productivity_change_percent'] = country_quarter_summary['avg_productivity_change_percent'].round(2)

# 5. Sortează frumos
country_quarter_summary = country_quarter_summary.sort_values(
    by=['country', 'survey_year', 'quarter']
).reset_index(drop=True)

# 6. Salvează într-un Excel cu un singur sheet
output_path = '/content/ai_reporting_local.xlsx'

with pd.ExcelWriter(output_path, engine='openpyxl') as writer:
    country_quarter_summary.to_excel(
        writer,
        sheet_name='country_quarter_summary',
        index=False
    )

print("Fișier Excel generat:", output_path)
print("Dimensiune tabel agregat:", country_quarter_summary.shape)

# 7. Preview
print(country_quarter_summary.head(10))

